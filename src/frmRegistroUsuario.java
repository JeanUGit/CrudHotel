    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
       
        if(!txtNombre.getText().equals("")&& !txtApellido.getText().equals("")&&!txtUsuario.getText().equals("")&&!txtPassword.getText().equals(""))
        {
            Coneccion cnx = new Coneccion();
            Connection cn = cnx.conectar();
            
            try{
                PreparedStatement pst = cn.prepareStatement("INSERT INTO usuarios(Nombre,Apellido,NombreUsuario,Contraseña)VALUES(?,?,?,?)");
                pst.setString(1, txtNombre.getText());
                pst.setString(2, txtApellido.getText());
                pst.setString(3, txtUsuario.getText());
                pst.setString(4, txtPassword.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null,"REGISTRADO CON EXITO");
                Limpiar();
                new frmLogin().setVisible(true);
                this.dispose();
            }catch(SQLException ex){
                 Logger.getLogger(frmRegistroUsuario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }else{
            JOptionPane.showMessageDialog(null,"DEBE LLENAR TODOS LOS CAMPOS");
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

